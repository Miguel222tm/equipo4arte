									<p style=" text-align: justify;">
									El neoclasicismo es una corriente que nace en el norte de Europa a finales del siglo XVIII, principalmente en Roma y Paris, sin embargo para el final de la corriente en 1830 ya se había extendido hasta el continente americano.
									</p>
										
									<p style=" text-align: justify;">
									Dentro del neoclasicismo no se busca solamente un ideal antiguo, sino que se buscaba realizar obras de arte con una perfección que no se había buscado antes de la manera más simple posible, dando como resultado obras tan perfectas técnicamente hablando que pudieran rayar en la frialdad.
									</p>
									<p style=" text-align: justify;">
									Dicha búsqueda de la sencillez y a la vez perfección llevo a que la mayoría de las esculturas se llevaran a cabo en mármol sin pintar. Dentro de esta arte se destacaron los desnudos y los retratos.
									</p>
									<p style=" text-align: justify;">
									Se buscaba también la verosimilizad de lo expresado en las obras de arte, si no se podía ver en la vida real no era digno de ser pintado, por lo que se evitaba lo insólito y anormal. Se buscaba que los elementos dentro de la pintura tuvieran una homogeneidad de principio en fin, tanto en colores como en técnicas y texturas, mientras que los elementos que servían de ambientación a la pintura debían respetar las reglas morales de la época.
									</p>
									<p style=" text-align: justify;">
									El Neoclasicismo buscaba reflejar la belleza y  la perfección formal  a partir de la vuelta a las raíces clásicas. Estaba inspirado  especialmente en las ideas de la ilustración que deseaban manifestar los valores eternos y universales.
									</p>
									<p style=" text-align: justify;">
									<Strong>Dentro de las principales curiosidades de dicha corriente se encuentran las siguientes: </Strong> <br>
•	Agrego a la pintura un valor didáctico y moralizante, considerando imprescindible el conocimiento científico de las fuentes clásicas.<br>
•	Buscan resucitar los modelos del Arte Antiguo, porque veían en ellos encarnadas la virtud y así lograr la perfección del mundo.<br>
•	Este estilo fue llamado “verdadero estilo”.<br>
•	El término “neoclásico”, no se empleó desde el principio, sino que se aplicó más tarde de forma despectiva, porque se consideró a esta corriente carente de originalidad.<br>
•	Las obras neoclásicas llevan una fuerte carga racionalista producto de la ilustración, es decir, que predomina lo que se puede entender y rechazan todo aquello que venga impuesto por la fe o la tradición.<br>
•	Promueven, fomentan y prevalecen los temas que manifiesten las ideas liberales de la época como la igualdad, la fraternidad y la libertad.

									</p>
									<p style=" text-align: justify;">
										El Neoclasicismo fue una vuelta a la Antigüedad Clásica, retomándola de forma mucho más fiel que la protagonizada por retornos anteriores. Se opuso a la funcionalidad y al exceso decorativo del Barroco y del Rococó. El Neoclasicismo se presentó como un arte nuevo, sereno y equilibrado, frente al agotamiento de las formas del Rococó, con su lenguaje vacío y reiterativo.
									</p>
