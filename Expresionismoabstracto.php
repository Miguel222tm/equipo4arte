									<p style=" text-align: justify;">
										El Expresionismo abstracto es un estilo artístico que surgió alrededor de 1910 y cuyas consecuencias múltiples han hecho de él una de las manifestaciones más significativas del espíritu del siglo XX. El arte abstracto deja de considerar justificada la necesidad de la representación figurativa y tiende a sustituirla por un lenguaje visual autónomo, dotado de sus propias significaciones. Este lenguaje se ha elaborado a partir de las experiencias fauvistas y expresionistas, que exaltan la fuerza del color y desembocan en la llamada abstracción lírica o informalismo, o bien a partir de la estructuración cubista, que da lugar a las diferentes abstracciones geométricas y constructivas.</p>
										<p style=" text-align: justify;">
										Pueden señalarse como características formales de este estilo, en primer lugar, su preferencia por los grandes formatos. Trabajaban normalmente con óleo sobre lienzo. Generalmente son abstractos en el sentido de que eliminan la figuración. No obstante, hay excepciones y algunos emplean trazos figurativos, apareciendo figuras reconocibles, como ocurre con las Mujeres de Willem de Kooning. Las telas presentan un aspecto geométrico que las hace diferentes de movimientos precedentes, como el surrealismo.</p>
								
										<p style=" text-align: justify;">
										Una de las características principales de los expresionistas abstractos es la concepción de la superficie de la pintura como all over (cobertura de la superficie), para significar un campo abierto sin límites en la superficie del cuadro: el espacio pictórico se trata con frontalidad y no hay jerarquía entre las distintas partes de la tela. El cromatismo suele ser muy limitado: blanco y negro, así como los colores primarios: magenta, amarillo y cian. Los pintores expresionistas que redujeron la obra a prácticamente un solo color, estaban ya anticipando el arte minimal.

									</p>
										<p style=" text-align: justify;">
										Este tipo de cuadros, con violentos trazos de color en grandes formatos, presenta como rasgos distintivos la angustia y el conflicto, lo que actualmente se considera que refleja la sociedad en la que surgieron estas obras.
									</p>

										
