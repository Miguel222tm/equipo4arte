									<p style=" text-align: justify;">
									Durante mediados del siglo XIX el arte ingles se vio estancado en las convenciones académicas, creando un callejón sin salida en el aspecto creativo. Como consecuencia de este hecho 3 jóvenes estudiantes de la Royal Academy fundan la cofradía prerrafaelista.
									</p>
										
									<p style=" text-align: justify;">
									Estos tres estudiantes eran Hunt, Mallais y Rossetti ambicionando crear una nueva clase de pintura refiriéndose ya no al renacimiento, sino al arte medieval, “Aquel anterior a Rafael” dando asi nacimiento a este nuevo estilo.
									</p>
									<p style=" text-align: justify;">
									El prerrafaelismo  es reconocido por sus cuadros llenos de color, que conllevan múltiples símbolos y referencias literarias, sensibles a la naturaleza y a las cuestiones sociales. En cuanto a la técnica, estos cuadros coinciden no solo por la gran vivacidad de los coloridos, sino también por  un realismo exacerbado que pretende ser una reproducción exacta de la naturaleza y una gran libertad, otorgada a las poses de los personajes. Todo ello acompañado por sabias referencias religiosas, literarias y poéticas.
									</p>
									<p style=" text-align: justify;">
									Más allá de una mera reacción de enseñanza académica,  los orígenes del prerrafaelismo solo se pueden comprender situándolos en el contexto político e intelectual de la época. 
									</p>
									<p style=" text-align: justify;">
									Este movimiento aunque inicialmente fue el objeto de virulentos ataques, (con motivo de las exposiciones de 1850 y de 1851) al ser reprochado  su tratamiento trivial de los temas sacros (en particular El Cristo en casa de sus padres de Millais), en el ámbito  literario proporciona un gran reconocimiento superando la desconformidad  social por gran parte del público que no perdona el repudio a la idealización
									</p>
									
