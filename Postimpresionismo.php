									<p style=" text-align: justify;">
									Bajo el nombre de Postimpresionismo o Neoimpresionismo se engloban múltiples tendencias y estilos. Viene a ser un crisol de diferentes técnicas y objetivos que responden a las inquietudes de los diferentes artistas.
									</p>
										
									<p style=" text-align: justify;">
									El postimpresionismo era tanto una extensión del impresionismo como un rechazo a sus limitaciones. Al igual que en la corriente impresionista se continuaron utilizando colores vivos, una aplicación compacta de la pintura, pinceladas distinguibles y temas de la vida real, pero a diferencia de los primeros se intentó llevar más emoción y expresión a su pintura.
									</p>
									<p style=" text-align: justify;">
									Con el postimpresionismo se creó un nuevo tipo de artista, el genio individual que vive de su arte, la personalidad creadora que plasma su psicología en el cuadro, gracias a su trazo singular, al color personal, y a la recuperación de las formas y las estructuras, pero con una nueva lectura. El color se hace representativo y subjetivo, no objetivo. Se representa el color con que se ve el alma de las cosas. La realidad se interpreta según la visión personal del genio creador.
									</p>
									<p style=" text-align: justify;">
									Algunas de las características principales del Postimpresionismo son:
									<br>
									•	Se concebía el arte como una actividad específica con reglas y realidad propia, distinta al entendimiento del arte como reproducción ilusoria de la realidad. <br>
									•	Existía un interés por la construcción de la forma, el dibujo y la expresividad ed los objetos y figuras humanas. <br>
									•	Conciliación entre efecto volumétrico y el gusto puramente estético.<br>
									•	Había una concepción del cuadro a base de cuerpos rigurosamente geométricos.<br>
									•	Se eliminó la perspectiva en profundidad, se descompuso la corporeidad de los objetos a elementos geométricos básicos y se creó la teoría científica de los colores denominada cromoluminarismo.<br>
									•	Se empleaban colores contrastantes para distender y definir planos y formas.<br>
									•	Se usaban efectos pictóricos basados en búsquedas estructurales, espaciales y cromáticas.<br>
									•	Existía un interés por lo exótico y los bajos fondos.

									</p>
									<p style=" text-align: justify;">
									El postimpresionismo supone entre otras cosas una recuperación de la importancia del dibujo y de la preocupación por captar no sólo la luz sino también la expresividad de las cosas y de las personas iluminadas. Su trascendencia para la evolución del arte es, si cabe, mayor que la de los pintores impresionistas.
									</p>
									
