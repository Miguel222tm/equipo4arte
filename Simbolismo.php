									<p style=" text-align: justify;">
									El Simbolismo es un movimiento literario y de artes plásticas que se originó en Francia en la década de 1880, paralelo al post-impresionismo, y que surgió como reacción al enfoque realista implícito en el Impresionismo. Tanto el Impresionismo, como el idealismo y el realismo se habían identificado con los problemas contemporáneos, políticos, morales e intelectuales. Los simbolistas disgustados por la incapacidad de la sociedad para resolver estos problemas buscaron nuevos valores basados en lo espiritual. Desean crear una pintura no supeditada a la realidad de su momento, rechazan lo que trae consigo la vida diaria, la aglomeración, la actividad industrial y la degradación.
									</p>
										
									<p style=" text-align: justify;">
									El Simbolismo pretendía restaurar significado al arte, que había quedado desprovisto de éste con la revolución impresionista. Mientras que otros neo-impresionistas se inclinaban por ramas científicas o políticas, el Simbolismo se decantaba hacia una espiritualidad frecuentemente cercana a posiciones religiosas y místicas.
									</p>
									<p style=" text-align: justify;">
									Con el simbolismo nació una división en el Arte Contemporáneo de dos tendencias opuestas. La primera, básicamente impresionista y cubista, considera la imagen como una realidad externa, concebida como un hecho existente fuera del individuo y respecto al cual este se tiene que situar. La segunda, que se identifica con el simbolismo y también con otras corrientes del arte abstracto , considera la imagen como la expresión de algo interior, de una idea, un pensamiento que tiene que ver con la personalidad y la expresividad del artista, y que está al margen de la realidad concreta del mundo que nos rodea.
									</p>
									<p style=" text-align: justify;">
									<Strong>Dentro de las principales características que se encuentran en el simbolismo están: </Strong>
									</p>
									<p style=" text-align: justify;">
									•	Se encuentran imágenes opuestas a la realidad visible, por lo tanto hay una realidad escondida la cual se basa en los sueños.<br>
									•	Sugiere ideas y emociones a partir de imágenes, y estas imágenes se expresan por medio de experiencias visuales y emocionales.<br>
									•	Todo tiene un alto contenido espiritual y religioso.<br>
									•	Utiliza el subjetivismo, lo irracional, la fantasía,  la intimidad y el uso de símbolos para comunicar emociones.<br>
									•	Este movimiento está en contra de los valores del materialismo y del pragmatismo.<br>
									•	Busca la verdad universal.<br>
									•	Es una tendencia que supera nacionalidades, límites cronológicos y estilos personales.<br>
									•	Busca el conocimiento intelectivo y la expresión conceptual.<br>
									•	Reflejan lo ambiguo la belleza hermafrodita, lo andrógino.<br>
									•	Creación de figuras mitológicas como sirenas, arañas, esfinges.<br>
									•	Plasman los sueños, fantasías para exteriorizar sus ideas.<br>
									•	La escultura ya no es rígida, dan una perspectiva de mayor movimiento y pliegues los cuales brindan ilusiones.

									</p>
									
