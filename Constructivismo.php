									<p style=" text-align: justify;">
									Movimiento artístico surgido en Rusia en 1917, que tiene sus raíces en los 'Vkhutemas', o "Talleres Superiores Artísticos y Técnicos del Estado" y se desarrolló principalmente en el arte, diseño y arquitectura rusos. 
									</p>
										
									<p style=" text-align: justify;">
									Se basa en el cubismo, y estéticamente se relaciona con la ingeniería y la arquitectura.
									</p>
									<p style=" text-align: justify;">
									La Revolución Soviética buscó nuevas formas de expresión relacionadas con la aspiración de suplantar el sistema capitalista por esquemas más democráticos de producción y distribución de bienes.
									</p>
									<p style=" text-align: justify;">
									Con esta finalidad, artistas como Vladimir Tatlin, Kasimir Malevich, Alexander Rodchenko, Wassily Kandinsky, Naum Gabo y El Lissitzky promovieron una estética y una aproximación al diseño que, entre otras cosas, se asociaba a la producción industrial y decía adiós al ornamento complaciente. 
									</p>
									<p style=" text-align: justify;">
									Por tanto, sus composiciones son construidas matemáticamente y sus motivos no son ni objetos reales ni fantasías libres del artista. Además, la valoración del espacio y el componente espacio/tiempo eran fundamentales.
									</p>
									<p style=" text-align: justify;">
									Estos artistas se inspiraban en la producción técnica desde un punto de vista estético. Abandonaron el ámbito puramente material y se dedicaron a todos los ámbitos artísticos: diseño de carteles, de moda, tipografías, fotografía, arquitectura interior, propaganda, ilustraciones.
									</p>
									<pstyle=" text-align: justify;"> 
									Entre las características de estilo está el uso de los colores naranjo, rojo, azul, amarillo, negro y blanco (generalmente siempre en los mismos tonos, tanto en afiches como en objetos), la constante alusión a elementos modernos que simbolizan el progreso, las estructuras geométricas y las formas pesadas.
									</p>
