									<p style=" text-align: justify;">
									Este movimiento surge a mediados del siglo XX  en Inglaterra y Estados Unidos como reacción artística ante el expresionismo abstracto al que se le consideraba vacío y elitista.  Caracterizado por el uso de objetos e imágenes cotidianas y urbanas, tomadas de los medios de comunicación, tales como anuncios publicitarios, comics, libros, objetos culturales y del mundo del cine.  La temática principal de estos se basaba en la sociedad de consumo así como la comunicación de masas y la aplicación del arte.
									</p>
										
									<p style=" text-align: justify;">
									El Arte pop busca utilizar imágenes conocidas con una dirección diferente para lograr una postura más estética o inclusive tomar una postura crítica a la sociedad de consumo tomando del pueblo sus intereses y temática.
									</p>
									<p style=" text-align: justify;">
									El Arte pop como su nombre lo indica es un “Arte popular” que muchas veces es ligado al uso de drogas, la música y a una sociedad con un consumismo exhaustivo. Pese a lo anterior este estilo maneja mensajes claros y sencillos que no buscan la originalidad, sino más bien la reinterpretación de pinturas, fotografías o grabados anteriores. Este estilo muestra diferentes propuestas artísticas en colores chillones siendo cómplices de la publicidad siendo entre otras cosas un interés más centrado en vender que en innovar artísticamente.
									</p>
									<p style=" text-align: justify;">
									Este tipo de arte trata de eliminar la brecha que existe entre los límites del arte “alto” y “bajo”. En donde  la aceptación del público se presentó gracias a sus formas fáciles y divertidas así como contenidos que podían ser captados sin dificultad.
									</p>
									<p style=" text-align: justify;">
									Como dice Roy Lichtenstein “Nosotros pensamos que la generación anterior intentaba alcanzar su subconsciente, mientras que los artistas pop intentamos distanciarnos de nuestra obra. Yo deseo que mi obra tenga un aire programado e impersonal, pero no creo ser impersonal mientras la realizo.”
									</p>
								
