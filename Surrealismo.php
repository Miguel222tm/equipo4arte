									<p style=" text-align: justify;">
									Considerado uno de los estilos artísticos más representativos del siglo XX, el surrealismo surgió en la década de 1920 como parte del avance de las vanguardias artísticas que buscaban representar ideales diferentes a los academicistas, quebrando las leyes de la pintura tradicional y logrando por tanto, llamar la atención del espectador de manera directa.
									</p>
										
									<p style=" text-align: justify;">
									El surrealismo se caracterizó por representar aquello que se observaba en la realidad de manera irreal, absurda o fantástica. En muchos casos, los cuadros surrealistas no son el producto de la realidad si no de los sueños y de las ideas no racionales que el artista poseía en su mente al momento de realizar la obra. Las obras no poseen una linealidad gráfica, los espacios están usualmente quebrados, las proporciones de las figuras no son reales y los colores muchas veces están invertidos.
									</p>
									<p style=" text-align: justify;">
									Para los surrealistas la obra nace del automatismo puro, es decir, cualquier forma de expresión en la que la mente no ejerza ningún tipo de control. Intentan plasmar por medio de formas abstractas o figurativas simbólicas las imágenes de la realidad más profunda del ser humano, el subconsciente y el mundo de los sueños.
									</p>
									<p style=" text-align: justify;">
									Se interesaron además por el arte de los pueblos primitivos, el arte de los niños y de los dementes. Preferirán los títulos largos, equívocos, misteriosos, lo que significa que importaba más el asunto que la propia realización.
									</p>
									<p style=" text-align: justify;">
									Algunas de las características más representativas de este movimiento fueron:
									<br>
•	Animación de lo inanimado<br>
•	Metamorfosis<br>
•	Aislamiento de fragmentos anatómicos<br>
•	Máquinas fantásticas<br>
•	Elementos incongruentes<br>
•	Perspectivas vacías<br>
•	Evocación del caos<br>
•	El sexo y lo erótico se trata de modo lúbrico<br>
•	Autómatas<br>
•	Espasmos<br>
•	Relaciones entre desnudos y maquinaria

									</p>
									<p style=" text-align: justify;">
									   El Surrealismo trataba de plasmar el mundo de los sueños y de los fenómenos subconscientes. Este sector recóndito del ser humano se considera apto para el análisis artístico. Breton intenta descubrir las profundidades del espíritu.
									</p>
									<p style=" text-align: justify;">
									Buscaba transformar la vida a través de la liberación de la mente del hombre de todas las restricciones tradicionales que la esclavizan.
									</p>
