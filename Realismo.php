									<p style=" text-align: justify;">
									El Realismo fue un movimiento artístico y literario cuyo propósito fue la representación objetiva de la realidad, basándose en la observación de los aspectos cotidianos que brindaba la época.
									Dicha característica, ajena a evocaciones o fantasías de corte romántico, posibilitaron a los artistas realistas un amplio campo de representación, tanto en la temática como en su intencionalidad.

									</p>
										
									<p style=" text-align: justify;">
									El Realismo no se reveló en Europa con igual intensidad ni tampoco de modo simultáneo. Su apogeo puede situarse entre 1840 y 1880, sin perjuicio de que en algunos países su práctica se prolongara hasta el primer cuarto del siglo XX. 
									El Realismo comenzó como un movimiento del proletariado artístico. La representación del pueblo tal como era encerraba un cierto mensaje sociopolítico. 

									</p>
									<p style=" text-align: justify;">
									Esa veracidad del Realismo fue entonces duramente criticada, acusándole de recrearse en lo feo y en lo vulgar, en lo morboso e, incluso, en lo obsceno. Se quiso ver, ante todo y sobre todo, que la representación de, por ejemplo, campesinos, o sea, de trabajadores vulgares, comportaba una protesta y, en definitiva, un ataque a la sociedad existente. 
									En ese compromiso con lo social, el Realismo dio paso a temas que hasta entonces se habían ignorado, elevando a la categoría de protagonistas de sus obras a tipos humanos que nunca tuvieron el honor de ser representados. Campesinos, picapedreros, ferroviarios, lavanderas, mineros, etc., fueron fuente de inspiración para los creadores realistas. 

									</p>
									<p style=" text-align: justify;">
									Bien es cierto que el compromiso social del Realismo no implicaba ninguna proclamación abierta y reivindicadora de mejoras sociales o de cambios políticos. Sin embargo, la decisión de reflejar ese tipo de realidades suponía un contundente testimonio e, incluso, un cierto compromiso. 
									Los temas tratados por los autores realistas no sólo conformaban un compendio veraz de la vida cotidiana del trabajador, revelando las injusticias sociales que se daban, sino que también expresaban el heroísmo de sus protagonistas. Esta categoría de héroe fue adjudicada progresivamente a otros sectores de la población que irrumpían en la vida moderna. 
									La gama de esa suerte de héroes modernos resultó muy amplia y variada, ya que incluía a todos aquellos individuos que de una forma u otra encarnaban los principales valores de su tiempo y su cultura. 

									</p>
									<p style=" text-align: justify;">
									El Realismo tocó también temas relacionados con la vida familiar y la intimidad, debido al empuje de los valores domésticos de la clase media experimentado a mediados del siglo XIX. 
									Pero el Realismo no se circunscribió al ámbito estrictamente rural o urbano o al exclusivamente social o heroico. También trató temas al aire libre, es decir, reuniones y meriendas campestres, escenas de playa y de hipódromo, etc., unas realidades vistas para la época de un modo absolutamente innovador. 

									</p>
									<p style=" text-align: justify;">
									El Realismo fue un arte que podría calificarse como sin estilo; pero un arte ampliamente cultivado. 
									</p>
