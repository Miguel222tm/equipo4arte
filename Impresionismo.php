									<p style=" text-align: justify;">
									El impresionismo es una corriente arte surgida en el siglo XIX, principalmente vinculada a la pintura: los pintores impresionistas retrataban objetos de acuerdo a la impresión que la luz produce a la vista y no según la supuesta realidad objetiva.
									</p>
										
									<p style=" text-align: justify;">
									Dicha corriente se mostraba en contra de las fórmulas artísticas impuestas por la Academia Francesa de Bellas Artes, que fijaba los modelos a seguir y patrocinaba las exposiciones oficiales en el Salón parisino. El objetivo de los impresionistas era conseguir una representación del mundo espontánea y directa. El Impresionismo parte del análisis de la realidad.
									</p>
									<p style=" text-align: justify;">
									Los impresionistas eliminaron los detalles minuciosos y tan sólo sugirieron las formas, empleando para ello los colores primarios (azul, rojo y amarillo) y los complementarios (naranja, verde y violeta). Consiguieron ofrecer una ilusión de la realidad aplicando directamente sobre el lienzo pinceladas de color cortas.
									</p>
									<p style=" text-align: justify;">
									Los artistas impresionistas solían pintar sin intención de ocultar las pinceladas fragmentadas. De esta forma demuestran cómo, bajo ciertas condiciones, la perspectiva permitía que distintas partes inconexas dieran lugar a un todo unitario.
									</p>
									<p style=" text-align: justify;">
									<strong>Algunas de las características primarias de este movimiento son: </strong>
									</p>
									<p style=" text-align: justify;">
									<strong>El paisaje como tema principal: </strong> El paisaje ofrece un campo donde todos los intereses de los impresionistas se ven concentrados: El aire libre, el contacto con la naturaleza y el encuentro con la luz. Dentro del paisaje, también es frecuente el tema de la representación de la nieve, el agua y el hielo.
									</p>
									<p style=" text-align: justify;">
									 <strong>Técnica: </strong> Los impresionistas se caracterizan por su técnica rápida, de largas pinceladas cargadas de pintura. Se dice que los últimos cuadros de Monet no son pinturas, sino más bien escultura sobre el lienzo.
									</p>
									<p style=" text-align: justify;">
									<Strong>Color: </Strong> Es significativo el que los impresionistas eliminen de su paleta el color negro, lo hacen porque observan que las sombras nunca son negras, sino coloreadas. Al igual, el blanco puro no existe, sino que la luz lo carga de matices innumerables. Apuestan por el color puro, aunque pueden permitirse mezclarlos directamente sobre la superficie del lienzo.
									</p>
									<p style=" text-align: justify;">
									<Strong> Ausencia de perspectiva: </Strong> Los impresionistas quitan el concepto de la perspectiva euclidiana que había regido el concepto de la pintura plana y dimensional porque en realidad es como percibe nuestra retina.
									</p>
