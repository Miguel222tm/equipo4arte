									<p style=" text-align: justify;">
									O conocido también como der blaue Reiter es una corriente que forma parte del expresionismo, buscando la expresión de los sentimientos y las emociones del autor más que una realidad objetiva.
									</p>
										
									<p style=" text-align: justify;">
									 Este movimiento consistía en una asociación informal de artistas en Munich Alemania datada en el año 1911. Sus principales expositores fueron los rusos Wassily Kandisky y Alexei von Jawlensky, los alemanres Franz Marc y August Macke y el suizo Paul Kele.
									</p>
									<p style=" text-align: justify;">
									Esta corriente se toma como más exquisita, subjetiva o inclusive espiritual, siendo la formulación de los artistas muy diferente, sin embargo los unía la convicción de mirar más allá de lo superficial.
									</p>
									<p style=" text-align: justify;">
									Estos artistas se enfocaban más en como exteriorizar algo, en otras palabras como plasmar o representar algo y no tanto en el “que” se pintaría. Algunos de los elementos que más tomaban importancia era el manejo de las formas y los colores, destacando el uso de tonos metálicos o aterciopelados y como su nombre lo indica el color azul.
									</p>
									<p style=" text-align: justify;">
									Una de las peculiaridades de esta corriente es que tiene una orientación internacionalista, ya que no existía una homogeneidad de los estilos. Sus pinturas exponían lo que sus autores llamaban “impulsos íntimos”.
									</p>
									<p style=" text-align: justify;">
									Muchas de sus obras exaltan la ingenuidad de la infancia, sin mostrar un gran interés por el arte naif (termino aplicado para calificar la producción pictórica, escultórica o arquitectónica, elaborada por una perdona que, por lo general, no posee una formación artística previa).
