									<p style=" text-align: justify;">
									El Art Nouveau o arte nuevo es  un movimiento artístico que surge a finales del siglo XIX y es mayormente proyectado  hasta inicios del siglo XX.  Este estilo es generalmente mejor expresado en los aspectos arquitectónicos, gráficos y decorativos.
									</p>
										
									<p style=" text-align: justify;">
									Sus orígenes datan en la época de la Belle époque en Europa y Estados Unidos. Toma su nombre a raiz de una exposición que realiza Munch en la galería parisina "La maison del Art Nouveau", diseñada por Siegfrid Bing (1838-1905), aunque se conoce con distintos nombres según los países: modernismo en España, Jugendstil en Alemania, Sezession (en Austria), Liberty en Inglaterra, Floreale en Italia, modernismo o Estilo modernista en Cataluña.
									</p>
									<p style=" text-align: justify;">
									Las características que más la distinguen de las otras corrientes son la adaptación a la que esta se puede ver sometida para conllevar las circunstancias de la vida moderna, así como su capacidad íntimamente ligada a la producción industrial, dividiéndose en 2 vertientes, la arquitectura y el diseño gráfico.
									</p>
									<p style=" text-align: justify;">
									De una manera más arcaica esta puede ser descrita como la unión entre las bellas artes y las artes aplicadas, dando hincapié a la creación de puertas, ventanas, muebles, joyería, lámparas, carteles, señales de tránsito entre muchos otros objetos con un estilo original y único, sin perder su funcionalidad.
									</p>
									<p style=" text-align: justify;">
									En cuanto al estilo de los contornos utilizados destaca el uso de formas y líneas onduladas, libres y casi nunca rectas, del desborde de detalles pero en un sentido calmado o elegante, de los colores, de las figuras femeninas cálidas y delicadas.


