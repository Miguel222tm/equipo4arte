									<p style=" text-align: justify;">
									
									El minimalismo es una corriente artística que sólo utiliza elementos mínimos y básicos. Por extensión, en el lenguaje cotidiano, se asocia el minimalismo a todo aquello que ha sido reducido a lo esencial y que no presenta ningún elemento sobrante o accesorio.

									</p>
										
									<p style=" text-align: justify;">
									La intención del minimalismo, por lo tanto, es generar sentido a partir de lo mínimo. Esto requiere simplificar los elementos utilizados, apelando a un lenguaje sencillo, colores puros y líneas simples.
									El minimalismo puede considerarse como la corriente artística contemporánea que utiliza la geometría elemental de las formas. Las formas son las que establecen una estrecha relación con el espacio que las rodea. Para ello el artista se fija solo en el objeto y aleja toda connotación posible. 

									</p>
									<p style=" text-align: justify;">
									Las obras del minimalismo buscan la sencillez y la reducción para eliminar toda alusión simbólica y centrar la mirada en cuestiones puramente formales como son el color, la escala, el volumen y el espacio circundante.
									</p>
									<p style=" text-align: justify;">
									Se habla del minimalismo refiriéndose a toda austeridad estilística, al énfasis en la geometría y la anulación de la técnica expresiva en las obras de tres dimensiones, principalmente.
Los minimalistas pretenden que sus obras sean como los números, moral y físicamente neutrales. Que no demuestren evidencia de trabajo, ya que no se valora la habilidad manual. Para el artista minimalista el trabajo es alienante, ya que rechazan el concepto heroico de la actividad creadora y sostienen que la labor física es lo menos importante del arte, preferirían absorber ideas más técnicas. 

									</p>
									<p style=" text-align: justify;">
									Las características básicas del estilo son:
									</p>
									<p style=" text-align: justify;">
									•	Abstracción total
									<br>
									•	Economía de lenguaje y medios
									<br>
									•	Producción y estandarización industrial
									<br>
									•	Uso literal de materiales
									<br>
									•	Austeridad con ausencia de ornamentos
									<br>
									•	Purismo estructural y funcional
									<br>
									•	Orden
									<br>
									•	Geometría elemental rectilínea
									<br> 
									•	Precisión en los acabados
									<br>
									•	Carácter opaco

									</p>
