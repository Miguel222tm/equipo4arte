									<p style=" text-align: justify;">
									Conocido también como Superrealismo, Realismo Fotográfico y Foto realismo, es un movimiento pictórico surgido en la década de los '60, cuyas técnicas aspiran a una precisión casi fotográfica. 
									</p>
										
									<p style=" text-align: justify;">
									El hiperrealismo es un estilo pictórico que nació en Estados Unidos tras el Pop Art y que se basa en la reproducción detallista y fotográfica de la realidad. También con este estilo existen el foto realismo y el realismo mágico, todos representando la realidad de modo realista y detallista pero con sus diferencias expresivas. La utilización de nuevas tecnologías, y sobre todo la fotografía, permiten al artista desarrollar este tipo de estilo muy difícil de realizar y que requiere un gran control de la técnica, tanto dibujo como color.
									</p>
									<p style=" text-align: justify;">
									El Hiperrealismo busca mantener en la pintura la conexión con la visión fotográfica del encuadre y la traducción fiel de la escena.
									</p>
									<p style=" text-align: justify;">
									Por tanto, los temas son representados con exactitud minuciosa e impersonal en los detalles, por medio de una agudeza óptica llevada a cabo con tal virtuosismo técnico, que supera la "visión" del objetivo fotográfico.
									</p>
									<p style=" text-align: justify;">
									El propósito del Hiperrealismo es acercarse a una supuesta objetividad visual en la representación de retratos, naturalezas muertas, interiores y paisajes, a través de un singular método de trabajo que compagina técnica y disciplina. 
									</p>
									<p style=" text-align: justify;">
									En América del Norte, donde el pop art había arelado profundamente, el hiperrealismo se trabajaba a partir de la reproducción pintada de los objetos de consumo y de las imágenes publicitarias correspondientes.
									</p>
									<p style=" text-align: justify;">
									En Europa, en cambio, el hiperrealismo no suele tener connotaciones del pop art y se impregna de un lirismo casi surrealista. 
									</p>
									<p style=" text-align: justify;">
									Desde que nació este empeño por copiar la fotografía y mejorarla gracias a la capacidad de la pintura, muchos artistas son los que continúan y dan sentido a este estilo. Sus obras sorprenden no solo por la capacidad pictórica que demuestran, también por cómo la visión del artista acaba modificando la realidad y haciéndola más potente, “hiper” realista, mucho más que lo que nuestros ojos sencillos pueden ver. 
									</p>
