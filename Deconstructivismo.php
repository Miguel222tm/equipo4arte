									<p style=" text-align: justify;">
									El Deconstructivismo también llamado deconstrucción, es un movimiento que surge en los años de 1960 en Francia y tiene su apogeo en los años de 1980 a 1990 y aun el 2000.
									</p>
										
									<p style=" text-align: justify;">
									Rompe principalmente con las funciones, las formas, los materiales y  con los volúmenes creando una confusión geométrica a gran escala.
									</p>
									<p style=" text-align: justify;">
									Se caracteriza por la fragmentación, el proceso de diseño no lineal y el uso de la geometría con formas no rectilíneas, que se emplean para distorsionar y dislocar algunos de los principios elementales de la arquitectura como la estructura y la envolvente del edificio. 
									</p>
									<p style=" text-align: justify;">
									Características Conceptuales:
									</p>
									<p style=" text-align: justify;">
									• Los representantes no ven sus obras como arte.
									<br>
									• Le dan relevancia a la técnica con la que se va elaborar el producto y el proceso que éste lleva.
<br>
									• Propuestas racionales basadas en la tecnología y la mecánica industrial.
<br>
									• Los artistas consideraban su trabajo del mismo modo que la actividad de los ingenieros, como un “construir” de objetos utilitarios, aunque frecuentemente éstos tuviesen que crear su propia “finalidad” (inventar un uso).
<br>
									• Fin social : La “reconstrucción” de la sociedad, lo que les condujo a un interés por la arquitectura, el diseño utilitario y la escenografía.


