									<p style=" text-align: justify;">
									El happening data de inicios de los 50’s, es una forma de arte que esta íntimamente relacionada con el teatro y que es desarrollada en un tiempo y espacio determinado, su estructura y contenido son la extensión lógica de los entornos. Es considerada como una manifestación artística multidisciplinaria.
									</p>
										
									<p style=" text-align: justify;">
									Una de sus características más destacadas es la participación e integración de los espectadores integrando el conjunto denominado “performance art” y manteniendo una estrecha afinidad con el llamado teatro de participación.
									</p>
									<p style=" text-align: justify;">
									Este tipo de arte tiene como tentativa el producir una obra en forma de evento más que algo focalizado en objetos, para eliminar la pasividad de los espectadores y contribuir en su actividad, logrando un alcance de liberación a través de la expresión emotiva y la representación colectiva.
									</p>
									<p style=" text-align: justify;">
									 Los hapenings son comúnmente producidos en lugares públicos de visita frecuente como símbolo de sorpresa o irrupción en la cotidianidad. El happening es guiado por la improvisación e intervención tanto de la música como de la danza, el teatro como elemento de expresión, no tiene acción solo acontecimientos marcados por el movimiento, luces o inclusive en algunos casos olores.
									</p>
									<p style=" text-align: justify;">
									Los happenings sin importar el o los involucrados será único y no se le podrá repetir, ya que este está basado en los movimientos, las expresiones, los escenarios y cualquier elemento presente en ese mismo instante, aunque se intenten repetir o recrear dichos elementos la más mínima variación es lo que los hace únicos y no les permite ser recreados.
									</p>
									<p style=" text-align: justify;">
									 Como menciona Allan Kaprow el denominado fundador del Hapening “La línea entre el arte y la vida debe mantenerse tan fluida, y quizás indistinta, como sea posible”
