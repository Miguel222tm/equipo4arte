									<p style=" text-align: justify;">
									Su origen proviene de la revolución Mexicana  en el año de 1910, sin embargo no es sino hasta 1921 cuando inicia formalmente como un movimiento artístico.  Durante esta primera mitad del siglo XX surgió la aparición de grandes pintores, cuyas obras se basan en retratar momentos heroicos sobre los edificios públicos.</p>
										<p style=" text-align: justify;">
										Su nacimiento se basa en tres características fundamentales:<br>
○	lo nacional<br>
○	Lo popular<br>
○	Lo revolucionario <br>
</p>
								
										<p style=" text-align: justify;">
										Estos tres valores en conjunto lograron imponer una lucha por la libertad de expresión y así mismo libero una opinión más abierta, valiente y fructífera que a fin de cuentas es la libertad de creación. 

									</p>
										<p style=" text-align: justify;">
								Los creadores de este movimiento “la pintura mural” expresaban mediante este medio sus antecedentes y objetivos.
									</p>

										<p style=" text-align: justify;">David Alfaro Siqueiros da importancia política a la huelga de estudiantes de la escuela de bellas artes así como la época de la revolución los cuales sin duda alguna fecundaron su experiencia de varios modos.</p>
										<p style=" text-align: justify;">
										En contraste, José Clemente Orozco representó una versión dignificada del mestizaje en Cortés y la Malinche. Dando un salto en el tiempo, pintó la Revolución poniendo al descubierto sus aspectos más trágicos, en obras de enorme fuerza dramática como La Trinchera o La Despedida.
									</p>
										<p style=" text-align: justify;">
										Aunque finalmente el muralismo mexicano surge de la necesidad de transformar el arte propio del pais, creando un movimiento nacional y popular con el fin de expresar o dar a conocer nuestra raza, el sufrimiento del pueblo durante la revolución entre otros aspectos. Proclamaba que los artistas debían producir “obras de valor ideológico para el pueblo, para todos, de educación y de batalla”.
									</p>
