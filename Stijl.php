									<p style=" text-align: justify;">
									También conocido como Neoplasticismo es como se conoce a un grupo de artistas y revista fundados en 1917 por Mondrian y Van Doesburg con el objetivo principal de dar a conocer los principios más importantes del neoplasticismo.
									</p>
										
									<p style=" text-align: justify;">
									El stijl es un estilo que modifico diversas áreas artísticas desde el diseño gráfico y la arquitectura hasta la pintura y la industria misma. Esta tendia a modificar los valores plásticos más puros y con una simplicidad notable, creando pinturas en tramas de líneas ortogonales que enmarcan colores primarios en formas de paralelogramos con lados y ángulos iguales o con los mismos ángulos pero diferentes medidas en sus lados.
									</p>
									<p style=" text-align: justify;">
									Aunque su contenido de colores se basaba en el uso de diversos tonos de amarillo, azul y rojo, estos llegaban a jugar con tonalidades de blanco, negro e inclusive grises. Generalmente adoptaban un fondo claro y ritmos asimétricos equilibrados con formas planas, rectas y simples.
									</p>
									<p style=" text-align: justify;">
									En cuanto a la arquitectura en combinación con el neoplasticismo se establecen criterios bastantes simples para su aplicación como que una cama debe ser capaz de encajar en la sala de al menos 2 diferentes posiciones. Otro es que todas las habitaciones en la casa deben tener suministro directo de agua y drenaje y finalmente cada habitación debe tener una puerta con acceso al exterior.
									</p>
									<p style=" text-align: justify;">
									Uno de los artistas que más influyo o afecto a este movimiento fue Mondrain quien eliminó todo indicio de los elementos representativos y evolucionó del cubismo hacia una abstracción geométrica pura.
									</p>
									
