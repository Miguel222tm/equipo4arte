									<p style=" text-align: justify;">
									
									El Futurismo es un movimiento literario y artístico que surge en Italia en el primer decenio del S. XX mientras el Cubismo aparece en Francia. Gira en torno a la figura de Marinetti, quien publica en el periódico parisiense Le Figaro el 20 de Febrero de 1909 el Manifiesto Futurista. Proclama el rechazo frontal al pasado y a la tradición, defendiendo un arte anti clasicista orientado al futuro, que respondiese en sus formas expresivas al espíritu dinámico de la técnica moderna y de la sociedad masificada de las grandes ciudades.

									</p>
										
									<p style=" text-align: justify;">
									En 1910 ya se puede hablar de un grupo liderado por Marinetti. Trabajarán artistas como los pintores Russolo, Carrá, Boccioni, Balla o Severini. El futurismo fue llamado así por su intención de romper absolutamente con el arte del pasado.
									</p>
									<p style=" text-align: justify;">
									La característica principal del futurismo es la plástica del dinamismo y del movimiento. El efecto de la dinámica se transmitía en vibrantes composiciones de color que debían producir un paralelismo multi sensorial de espacio, tiempo y sonido. Al principio, se valieron para la realización de sus objetivos artísticos de la técnica divisionista, heredada del neoimpresionismo y más tarde se aplicó la técnica cubista de abstracción como procedimiento para desmaterializar los objetos. A partir de estas premisas, la representación del movimiento se basó en el simultaneismo, es decir, multiplicación de las posiciones de un mismo cuerpo, plasmación de las líneas de fuerza, intensificación de la acción mediante la repetición y la yuxtaposición del anverso y del reverso de la figura.
									</p>
									<p style=" text-align: justify;">
									Buscaban por todos los medios reflejar el movimiento, la fuerza interna de las cosas, ya que el objeto no es estático. La multiplicación de líneas y detalles, semejantes a la sucesión de imágenes de un caleidoscopio o una película, pueden dar como resultado la impresión de dinamismo. Crearon ritmos mediante formas y colores. En consecuencia, pintan caballos, perros y figuras humanas con varias cabezas o series radiales de brazos y piernas. El sonido puede ser representado como una sucesión de ondas y el color como una vibración de forma prismática.
									</p>
									
