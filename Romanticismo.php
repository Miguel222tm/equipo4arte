									<p style=" text-align: justify;">
									
									Es la tendencia cultural que se impuso en Europa durante la primera mitad del siglo XIX. Esta significó una reacción contra la corriente neoclásica. A lo largo de la 2ª mitad del siglo XIX el romanticismo sería desplazado por el realismo.

									</p>
										
									<p style=" text-align: justify;">
									Resulta complicado definir al romanticismo, aunque sea incuestionable su gran influencia dentro de la historia revolucionaria. Esta corriente propone una ruptura total de tradiciones, jerarquías y toda una cultura anterior para dar paso a una total libertad dentro de todas y cada una de las bellas artes que se conocen.
									</p>
									<p style=" text-align: justify;">
									Aunque la principal característica de este movimiento se basa en el sentir, las emociones y en la concepción del hombre como ser sentimental, es cierto que el romanticismo varia dentro de las naciones y más aun de país a país, ejemplo de esto encontramos el romanticismo francés, el cual tiene determinada orientación religiosa y teórica, mientras que el romanticismo alemán es más duro y menos teórico.
									</p>
									<p style=" text-align: justify;">
									El romántico transforma el instinto en arte y el inconsciente en saber. Aspira a la fusión del sujeto con la obra artística, del "yo" en el "no-yo", del Uno con el Todo. Crear significa aproximarse a su verdad, a la última dimensión del ser.
									</p>
									<p style=" text-align: justify;">
									El romántico se rebela ante la escisión del hombre moderno entre el Yo y la alteridad, entre el sujeto y el objeto. El conflicto del hombre romántico, el "mal del siglo", su crisis religiosa y existencial es consecuencia de su propia singularidad y de la imposibilidad de fundir su Yo con la alteridad, con el Todo; de, siendo finito, desear unirse y transformarse en infinito.
									</p>
									<p style=" text-align: justify;">
									El romántico hereda del ilustrado el tedio de la civilización, que produce seres artificiales e insensibles; hereda la admiración hacia el primitivismo del buen salvaje, estropeado, según Rousseau, por la cultura y la civilización. Al romántico le asusta el futuro que la ciencia y el progreso anuncian, y quisiera volver a la plena integración con la Naturaleza, al árbol de la vida, que existió en el Paraíso antes de que se interpusiera el árbol de la ciencia.
									</p>
									<p style=" text-align: justify;"> <Strong>Etapas </Strong></p>
									<p style=" text-align: justify;">
										El romanticismo constituyó un movimiento contradictorio pues en su seno se desarrollaron dos sensibilidades diferentes a lo largo de dos etapas:
									</p>
									<p style=" text-align: justify;">
									<Strong>Hasta 1825 </Strong> <br>
									El romanticismo actuó como una tendencia conservadora y reaccionaria frente a la modernidad. Lo caracterizaron los viejos ideales basados en la jerarquía, la religión tradicional y la monarquía absoluta, antitéticos de la Ilustración.
									<br>
									Tres fueron las figuras más representativas de este período: Schlegel, De Maistre y Bonald.  
									</p>
									<p style=" text-align: justify;">
									<Strong>	A partir de 1825 </Strong><br>
									Impulsado por una segunda generación de autores el movimiento se identificó con los valores del progreso y la libertad, descollando las figuras de Víctor Hugo y Lamartine, muy comprometidos con la acción política de la Revolución de 1848.

									</p>

