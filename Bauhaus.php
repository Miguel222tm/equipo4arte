									<p style=" text-align: justify;">
									La Bauhaus fue una escuela alemana en la que se unificaron las artesanías, diseño, arte y arquitectura. Esta escuela nació de la unión entre la escuela de Bellas Artes con la escuela de Artes y Oficios gracias al arquitecto Walter Gropius.
									</p>
										
									<p style=" text-align: justify;">
									El objetivo de dicha institución fue la recuperación de los métodos artesanales en la actividad constructiva así como elevar la potencia artesanal al mismo nivel que el de las bellas artes e intentar comercializar los productos que, integrados en la producción industrial se convertirían en objeto de consumo asequibles para el gran público.
									</p>
									<p style=" text-align: justify;">
									Algunas de las características que la hacen destacar entre las demás es que perseguía la armonía del arte y la industria de tal manera que se creara una fusión entre la funcionalidad y el arte, permitiendo que los artistas entren en la sociedad como un personaje útil que interviene directamente en ella.
									</p>
									<p style=" text-align: justify;">
									En esta escuela se enseñaba a crear. El trabajo manual era la base del trabajo del artista que utilizaba formas y colores básicos, buscando una calidad no una novedad efímera. La belleza principal del objeto se basaba en el material y no en ornamentos.
									</p>
									<p style=" text-align: justify;">
									Poco a poco el estilo se fue internacionalizando debido a la acelerado industrialización en las sociedades de Europa y Norte América creadores de edificios con oficinas, bloque de departamentos, nuevas fábricas entre muchas otras.
									</p>
									<p style=" text-align: justify;">
									Otros agentes que ungieron la función de catalizadores fueron los enormes avances técnicos en la ciencia de los materiales y la construcción, creando nuevas posibilidades estructurales que creaban, o más bien, mostraban más resistencia y espacios más abiertos.
									</p>
